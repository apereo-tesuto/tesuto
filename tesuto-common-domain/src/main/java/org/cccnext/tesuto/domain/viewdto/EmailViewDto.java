package org.cccnext.tesuto.domain.viewdto;

import org.cccnext.tesuto.domain.dto.ViewDto;

/**
 *
 * @author Richard Scott Smith <scott.smith@isostech.com>
 */
public class EmailViewDto implements ViewDto {
	private String name;
    private String email;
    private String comments;
    private String technicalData;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @return the comments
	 */
	public String getComments() {
		return comments;
	}

	/**
	 * @param comments the comments to set
	 */
	public void setComments(String comments) {
		this.comments = comments;
	}

	/**
	 * @return the technicalData
	 */
	public String getTechnicalData() {
		return technicalData;
	}

	/**
	 * @param technicalData the technicalData to set
	 */
	public void setTechnicalData(String technicalData) {
		this.technicalData = technicalData;
	}
	
}
