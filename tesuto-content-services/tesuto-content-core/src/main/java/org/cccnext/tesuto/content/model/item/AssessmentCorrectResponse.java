package org.cccnext.tesuto.content.model.item;

import java.util.List;

import org.cccnext.tesuto.content.model.AbstractAssessment;

/**
 * TODO: Implement DefaultValue here?
 *
 * @author Richard Scott Smith <scott.smith@isostech.com>
 */
public class AssessmentCorrectResponse implements AbstractAssessment {
    private static final long serialVersionUID = 1l;

    private String description;
    private List<String> values;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<String> getValues() {
        return values;
    }

    public void setValues(List<String> values) {
        this.values = values;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((description == null) ? 0 : description.hashCode());
        result = prime * result + ((values == null) ? 0 : values.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        AssessmentCorrectResponse other = (AssessmentCorrectResponse) obj;
        if (description == null) {
            if (other.description != null)
                return false;
        } else if (!description.equals(other.description))
            return false;
        if (values == null) {
            if (other.values != null)
                return false;
        } else if (!values.equals(other.values))
            return false;
        return true;
    }
}
