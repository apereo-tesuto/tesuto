package org.cccnext.tesuto.content.model.competency;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;
import org.cccnext.tesuto.content.model.AbstractAssessment;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.CompoundIndex;
import org.springframework.data.mongodb.core.index.CompoundIndexes;
import org.springframework.data.mongodb.core.mapping.Document;

import java.util.List;

/**
 * Created by jasonbrown on 6/17/16.
 */
@Document // This is for Mongo
@CompoundIndexes({
        @CompoundIndex(name = "discipline_version_idx", def = "{'discipline' : 1, 'version' : -1}", unique = true),
        @CompoundIndex(name = "discipline_version_published_idx_1", def = "{'discipline' : 1, 'version' : -1, 'published' : 1}", unique = true) })
public class CompetencyMap implements AbstractAssessment {
    private static final long serialVersionUID = 2l;

    @Id
    private String id; // needed for Mongo
    private String title;
    private int version;
    private String identifier;
    private String discipline;
    private List<CompetencyRef> competencyRefs;

    private boolean published;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdentifier() {
        return identifier;
    }

    public void setIdentifier(String identifier) {
        this.identifier = identifier;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }

    public String getDiscipline() {
        return discipline;
    }

    public void setDiscipline(String discipline) {
        this.discipline = discipline;
    }

    public List<CompetencyRef> getCompetencyRefs() {
        return competencyRefs;
    }

    public void setCompetencyRefs(List<CompetencyRef> competencyRefs) {
        this.competencyRefs = competencyRefs;
    }

    public boolean isPublished() {
        return published;
    }

    public void setPublished(boolean published) {
        this.published = published;
    }

    @Override
    public boolean equals(Object o) {
        return EqualsBuilder.reflectionEquals(this, o);
    }

    @Override
    public int hashCode() {
        return HashCodeBuilder.reflectionHashCode(this);
    }

    @Override
    public String toString() {
        return ToStringBuilder.reflectionToString(this);
    }
}
